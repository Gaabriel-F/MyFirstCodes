def Half(value):
    return value / 2


def Double(value):
    return value * 2


def ReduceByPercent(value, percent):
    percent1 = (value * percent) / 100
    Reducing = value - percent1    
    return Reducing


def AddByPercent(value, percent):
    percent1 = (value * percent) / 100
    Adding = value - percent1    
    return Adding
